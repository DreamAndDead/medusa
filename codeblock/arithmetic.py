# arithmetic 算术运算

# + - * /
a = 1 + 2
assert a == 3

a = a - 4
assert a == -1

a = a * 10
assert a == -10

a = a / 4
assert a == -2.5


# floordiv
b = 10 // 4
assert b == 2


# mod
m = 100 % 3
assert m == 1


# pow
p = 2 ** 10
assert p == 1024


# positive and negative
n = +1 ++++ +1
assert n == 2

n = -1 ---- -1
assert n == -2

